#assign a grade to a student based on user input
user_in=int(input("Enter your number -> "))
if(user_in>=90 and user_in<100):
    print("You got A+ ")
elif(user_in>=80 and user_in<90):
    print("You got A ")
elif(user_in>=70 and user_in<80):
    print("You got B+ ")
elif(user_in>=60 and user_in<70):
    print("You got B ")
